/*
 * scroll.h - 画面下部の帯状スクロール(横スクロールテロップ)
 *
 * 全角(2バイトSJIS)文字を1バイトずつずらして描画すると文字の途中で
 * 分割されて文字化けするため、スクロール文字列は半角カナ+半角英数のみで
 * 構成する(1文字=1バイト=1桁になり、1桁単位でスクロールしても崩れない)。
 * これは実機PC-98時代のデモでも定番の手法。
 */
#ifndef SCROLL_H
#define SCROLL_H

/* msg: 半角文字のみで構成された'\0'終端文字列(CP932変換前提、$は含めないこと) */
void scroll_init(const char *msg);

/* 1コマ分スクロールし、画面25行目(最下行)に描画する */
void scroll_step(void);

#endif
